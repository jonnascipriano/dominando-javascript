// Escreva um programa que exiba os números de 1 a 10 utilizando um laço de repetição

let i = 1

// Utilizando um laço de repetição while
// enquanto i for menor ou igual a 10, exiba o valor de i e incremente o valor de i +1
while (i <= 100){
    document.querySelector('p').innerHTML += `${i}<br>`
    // console.log(i)
    i++
}